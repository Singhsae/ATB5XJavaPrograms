package src.basic_03;

public class Lab0014 {
    public static void main(String[] args) {
        //OR operator
        System.out.println(true || true);
        System.out.println(true || false);
        System.out.println(false || true);
        System.out.println(false || false);

        //AND operator
        System.out.println(true && false);
        System.out.println(true && true);
        System.out.println(false && false);
        System.out.println(false && true);
    }
}
